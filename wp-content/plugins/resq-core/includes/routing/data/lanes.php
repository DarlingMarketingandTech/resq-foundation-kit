			foreach ( $registry as $lane_key => $lane ) {
				if ( isset( $lane['status'] ) && 'draft' === $lane['status'] ) {
					$registry[ $lane_key ]['status'] = 'approved';
				}
			}
		}

		return apply_filters( 'resq_lane_registry', $registry );
	}
}
